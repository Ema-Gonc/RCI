#!/bin/bash

# Comprehensive test script for 5-node OWR distributed routing system
# Demonstrates full network topology with routing and messaging

echo "=== OWR 5-Node Distributed Routing System Test ==="
echo "This test creates a complete network with 5 nodes and tests all functionality"
echo ""

# Node configuration
NODES=(
    "127.0.0.1:8080:01"
    "127.0.0.1:8081:02"
    "127.0.0.1:8082:03"
    "127.0.0.1:8083:04"
    "127.0.0.1:8084:05"
)

echo "Network Topology:"
echo "Node 01 (8080) <-> Node 02 (8081) <-> Node 03 (8082)"
echo "Node 02 (8081) <-> Node 04 (8083)"
echo "Node 03 (8082) <-> Node 05 (8084)"
echo ""

echo "=== PHASE 1: Start All Nodes ==="
i=0
for node in "${NODES[@]}"; do
    IFS=':' read -r ip port id <<< "$node"
    echo "Terminal $((i+1)): Start Node $id"
    echo "  ./owr $ip $port"
    echo "  Then type: dj 100 $id"
    echo ""
    ((i++))
done

echo "=== PHASE 2: Establish Connections ==="
echo "Connect in this order:"
echo ""
echo "Node 01 -> Node 02:"
echo "  In Node 01 terminal: dae 02 127.0.0.1 8081"
echo ""
echo "Node 02 -> Node 01:"
echo "  In Node 02 terminal: dae 01 127.0.0.1 8080"
echo ""
echo "Node 02 -> Node 03:"
echo "  In Node 02 terminal: dae 03 127.0.0.1 8082"
echo ""
echo "Node 03 -> Node 02:"
echo "  In Node 03 terminal: dae 02 127.0.0.1 8081"
echo ""
echo "Node 02 -> Node 04:"
echo "  In Node 02 terminal: dae 04 127.0.0.1 8083"
echo ""
echo "Node 04 -> Node 02:"
echo "  In Node 04 terminal: dae 02 127.0.0.1 8081"
echo ""
echo "Node 03 -> Node 05:"
echo "  In Node 03 terminal: dae 05 127.0.0.1 8084"
echo ""
echo "Node 05 -> Node 03:"
echo "  In Node 05 terminal: dae 03 127.0.0.1 8082"
echo ""

echo "=== PHASE 3: Test Routing Convergence ==="
echo "After connections are established, test routing:"
echo ""
echo "In Node 01: a  (announce routes)"
echo "In Node 05: a  (announce routes)"
echo ""
echo "Check routes in each node:"
echo "  sr 01  (from any node)"
echo "  sr 05  (from any node)"
echo "  sr 03  (from Node 01 - should go 01->02->03)"
echo ""

echo "=== PHASE 4: Test Message Routing ==="
echo "Test multi-hop messaging:"
echo ""
echo "From Node 01 to Node 05 (should route: 01->02->03->05):"
echo "  In Node 01: m 05 Hello from Node 01 to Node 05"
echo ""
echo "From Node 05 to Node 01 (should route: 05->03->02->01):"
echo "  In Node 05: m 01 Hello from Node 05 to Node 01"
echo ""
echo "From Node 04 to Node 05 (should route: 04->02->03->05):"
echo "  In Node 04: m 05 Hello from Node 04 to Node 05"
echo ""

echo "=== PHASE 5: Test Failure Handling ==="
echo "Test network resilience:"
echo ""
echo "Remove critical link (Node 02 - Node 03):"
echo "  In Node 02: re 03"
echo "  In Node 03: re 02"
echo ""
echo "Check route updates:"
echo "  In Node 01: sr 05  (should find new path or infinite cost)"
echo "  In Node 05: sr 01  (should trigger coordination)"
echo ""
echo "Reconnect:"
echo "  In Node 02: dae 03 127.0.0.1 8082"
echo "  In Node 03: dae 02 127.0.0.1 8081"
echo ""

echo "=== PHASE 6: Cleanup ==="
echo "Exit all nodes:"
echo "  In each terminal: x"
echo ""

echo "=== Expected Results ==="
echo "✓ All nodes should discover routes to each other"
echo "✓ Messages should be delivered via shortest paths"
echo "✓ Route costs should be correct (hops)"
echo "✓ Coordination should prevent loops during failures"
echo "✓ Routes should reconverge after topology changes"
echo ""

echo "=== Automation Note ==="
echo "For automated testing, you could use 'expect' scripts or tmux"
echo "to control multiple terminals simultaneously."
echo ""
echo "Example expect script structure:"
echo "  spawn ./owr 127.0.0.1 8080"
echo "  expect \"Type commands\""
echo "  send \"dj 100 01\\r\""
echo "  expect \"joined\""
echo "  # ... continue with commands"
echo ""

echo "The 5-node test validates the distributed routing algorithm's"
echo "ability to handle complex topologies and dynamic changes."